#for loops with list
list = ["akshat", "harry", "mamta", "Riddhi", "vanshika", "khushi"]
for i in list:
    print(i)

#for loops with tuple
tuple = ("akshat", "harry", "mamta", "Riddhi", "vanshika", "khushi")
for i in tuple:
    print(i)

#for loops with string
string = "Akshat"
for i in string:
    print(i)